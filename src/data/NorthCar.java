
package data;

public class NorthCar {
    
    //crio atributos da classe 
    private double precoSemana;
    private double precoFimSemana;
    private int capacidade;
    private double qtdCarros;

    public double getPrecoSemana() {
        return precoSemana;
    }

    public void setPrecoSemana(double precoSemana) {
        this.precoSemana = precoSemana;
    }

    public double getPrecoFimSemana() {
        return precoFimSemana;
    }

    public void setPrecoFimSemana(double precoFimSemana) {
        this.precoFimSemana = precoFimSemana;
    }

    public int getCapacidade() {
        return capacidade;
    }

    public void setCapacidade(int capacidade) {
        this.capacidade = capacidade;
    }

    public double getQtdCarros() {
        return qtdCarros;
    }

    public void setQtdCarros(double qtdCarros) {
        this.qtdCarros = qtdCarros;
    }
    
    
    
}
