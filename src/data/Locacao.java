
package data;


public class Locacao {
    
    private long totalDias;
    private long totalDiasSemana;
    private long totalDiasFimSemana;
    private double totalSouth;
    private double totalWest;
    private double totalNorth;
    

    public long getTotalDias() {
        return totalDias;
    }

    public void setTotalDias(long totalDias) {
        this.totalDias = totalDias;
    }

    public long getTotalDiasSemana() {
        return totalDiasSemana;
    }

    public void setTotalDiasSemana(long totalDiasSemana) {
        this.totalDiasSemana = totalDiasSemana;
    }

    public long getTotalDiasFimSemana() {
        return totalDiasFimSemana;
    }

    public void setTotalDiasFimSemana(long totalDiasFimSemana) {
        this.totalDiasFimSemana = totalDiasFimSemana;
    }

    public double getTotalSouth() {
        return totalSouth;
    }

    public void setTotalSouth(double totalSouth) {
        this.totalSouth = totalSouth;
    }

    public double getTotalWest() {
        return totalWest;
    }

    public void setTotalWest(double totalWest) {
        this.totalWest = totalWest;
    }

    public double getTotalNorth() {
        return totalNorth;
    }

    public void setTotalNorth(double totalNorth) {
        this.totalNorth = totalNorth;
    }
    
    
}
